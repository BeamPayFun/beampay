#!/usr/bin/env node
/**
 * Sync the contract reference from the beampay-contracts NatSpec.
 *
 *   1. run `forge doc` in ../beampay-contracts into a temp book dir.
 *   2. lift the single BeamPayRouter page out of forge's mdbook scaffold (SUMMARY.md,
 *      per-dir READMEs, css/js are discarded — they read poorly on GitHub).
 *   3. light post-process → docs/contracts/reference.md.
 *
 * forge doc already emits one clean self-contained page per contract, so this is a
 * lift-and-retitle, not a heavy transform. Assumes the sibling-clone layout.
 *
 * Usage:  node scripts/sync-contract-docs.mjs
 */
import { execFileSync } from 'node:child_process'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { tmpdir } from 'node:os'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const contractsRepo = resolve(repoRoot, '..', 'beampay-contracts')
const bookDir = join(tmpdir(), 'beampay-forge-doc')
const refOut = join(repoRoot, 'docs', 'contracts', 'reference.md')
// forge mirrors the source tree under <out>/src; the router page lives here:
const pagePath = join(bookDir, 'src', 'src', 'BeamPayRouter.sol', 'contract.BeamPayRouter.md')

if (!existsSync(contractsRepo)) {
  console.error(`beampay-contracts not found at ${contractsRepo} (expected sibling-clone layout)`)
  process.exit(1)
}

console.error(`> forge doc in ${contractsRepo}`)
execFileSync('forge', ['doc', '--out', bookDir], {
  cwd: contractsRepo,
  stdio: ['ignore', 'inherit', 'inherit'],
})

if (!existsSync(pagePath)) {
  console.error(`expected forge doc page not found: ${pagePath}`)
  process.exit(1)
}

const banner =
  '<!-- GENERATED by scripts/sync-contract-docs.mjs via `forge doc`. Do not edit by hand. -->\n\n'
const intro =
  '> Generated from the `BeamPayRouter.sol` NatSpec. For deployed addresses see ' +
  '[addresses.md](./addresses.md); for the load-bearing security properties see ' +
  '[invariants.md](./invariants.md).\n\n'

let md = readFileSync(pagePath, 'utf8')
md = md.replace(/^# BeamPayRouter\b.*$/m, '# BeamPayRouter — Contract Reference')

writeFileSync(refOut, `${banner}${intro}${md.trimEnd()}\n`)
console.error(`wrote ${refOut}`)
